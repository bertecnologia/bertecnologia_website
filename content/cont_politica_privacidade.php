                    <p><strong>6. Compartilhamento dos seus dados pessoais</strong></p>

                    <p>Podemos, ocasionalmente, compartilhar os seus dados pessoais para terceiros, tais como prestadores de serviços de tecnologia e infraestrutura, que poderão oferecer anúncios nas Plataformas Digitais, compartilhando conosco alguns de seus dados pessoais para uma melhor experiência no uso dos nossos serviços, sempre respeitando a transparência e as finalidades previstas pela Lei Geral de Proteção de Dados (LGPD).</p>

                    <p><strong>Serviços de terceiros. </strong>Seus dados pessoais e informações também poderão ser compartilhados com prestadores de serviços, sempre sujeito às obrigações consistentes com esta Política de Privacidade, tais como empresas provedoras de infraestrutura tecnológica e operacional, empresas de atendimento ao consumidor, empresas de comunicação de serviços, empresas intermediadoras de pagamento e empresas e provedores de serviço de armazenamento de informações e demais parceiros necessários para a completa atividade da B&R. Esse compartilhamento é essencial para que você possa melhor utilizar nossos serviços.
                    </p>

                    <p><strong>Anúncios. </strong>Ao usar nossas Plataformas Digitais, você concorda que a B&R e seus parceiros podem utilizar parte de seus dados pessoais para disponibilizar anúncios que consideramos relevantes para você e seus interesses nas Plataformas Digitas ou em conexão com a exibição de conteúdo. Para o fornecimento de anúncios mais customizados e eficazes, nossos parceiros podem compartilhar conosco algumas de suas informações pessoais, em especial dados de navegação, localização e interesse, permitindo que os combinemos com dados pessoais já coletados. A receita oriunda de publicidade permite a B&R continuadamente melhore seus serviços.
                    </p>

                    <p><strong>Outros compartilhamentos. Poderemos usar e compartilhar dados pessoais ou outros dados de segurança se acreditarmos que são razoavelmente necessários para: </strong></p>

                    <ul>
                        <li>Cumprimento de obrigação legal ou regulatória, incluindo ordem judicial ou
                            requerimento de autoridades administrativas que detenham competência
                            legal para tal requisição.
                        </li>

                        <li>Exercício regular do direito da B&R, incluindo proteção dos interesses da
                            B&R em qualquer tipo de conflito, incluindo ações judiciais.
                        </li>

                        <li>Proteger a segurança de qualquer pessoa.</li>

                        <li>Proteger a segurança ou integridade de nossa plataforma.</li>

                        <li>Proteger você ou terceiros de fraude.</li>

                        <li>Proteger direitos ou nossa propriedade ou de terceiros que usam nossos
                            serviços.
                        </li>

                        <li>Casos de transações e alterações societárias envolvendo a B&R, hipótese
                            em que a transferência das informações será necessária para a
                            continuidade dos serviços e utilização das Plataformas Digitais.
                        </li>

                        <li>Envio de notificações não obrigatórias por e-mail, aplicativos de
                            mensagens, SMS e notificação push.
                        </li>

                        <li>Cumprir com o legítimo interesse da B&R e das empresas que integrem o
                            grupo empresarial da B&R para os mesmos fins indicados na presente
                            Política de Privacidade.
                        </li>

                        <li>Em caso de exceções não previstas nos itens acima, a B&R aplicará o termo
                            de consentimento.
                        </li>
                    </ul>
