                    <p><strong>7. Seus direitos como titular dos dados pessoais</strong></p>

                    <p>Você poderá exercer seus direitos de titular de dado pessoal, tais como requerer acesso aos dados pessoais que coletamos, assim como requerendo retificação, atualização, portabilidade, revogação de consentimento e eliminação de seus dados pessoais.</p>

                    <p><strong>Direitos de titular. </strong>Enquanto titular de dados pessoais, você poderá exercer uma série de direitos. Disponibilizaremos mecanismos para que você possa entender de forma clara e transparente como poderá exercer seus direitos. A B&R empreenderá todos os esforços para atender tais pedidos no menor espaço de tempo possível.</p>

                    <p><strong>Acesso a dados. </strong>Você poderá solicitar através do canal exclusivo para esta finalidade, a qualquer momento, que a B&R lhe informe e confirme a existência de tratamento de dados em nossas Plataformas Digitais ou demais bases sob a responsabilidade da emissora, bem como forneça os dados que possuímos de você.
                    </p>

                    <p><strong>Retificação e atualização de dados. </strong>Se você identificar que os seus dados pessoais necessitam de correções ou se encontrarem incompletos, inexatos ou desatualizados, você poderá, caso não haja possibilidade de fazê-lo diretamente em nossas Plataformas Digitais, encaminhar solicitação para o canal dedicado ao titular dos dados. Lembre-se que você pode ter de comprovar, inclusive por meio de documento, a veracidade desses dados.
                    </p>

                    <p><strong>Demais direitos. </strong>A B&R respeita e garante a você a possibilidade de apresentação de solicitações baseadas nos seguintes direitos:
                    </p>

                    <ul>
                        <li>Anonimato, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade.</li>

                        <li>Portabilidade de seus dados a outro fornecedor de serviço ou produto</li>

                        <li>Eliminação dos dados tratados com o seu consentimento.</li>

                        <li>Informação sobre a possibilidade de não fornecer o seu consentimento, bem como de ser informado sobre as consequências em caso de negativa.</li>
                    </ul>
                    <br>

                    <p><strong>8. Coleta e revogação do consentimento</strong></p>

                    <p>Poderemos pedir seu consentimento para alguns dos usos desses dados pessoais. Você tem o direito de não dar seu consentimento, assim como de revogá-lo, mas a B&R poderá ainda fazer uso de parte desses dados pessoais, nas hipóteses previstas em lei.</p>

                    <p><strong>Obtenção de consentimento. </strong>Caso seu consentimento seja necessário para uso dos seus dados pessoais, de acordo com o disposto na lei aplicável, ele será coletado eletronicamente, momento em que serão informados os procedimentos e finalidades. Você possui o direito de não oferecer o seu consentimento ou de solicitar maiores esclarecimentos acerca desse consentimento.
                    </p>

                    <p><strong>Revogação do consentimento. </strong>Você poderá solicitar a revogação do consentimento que concedeu. Esta revogação poderá ocasionar a impossibilidade de uso ou acesso de funcionalidades dos sistemas e serviços da B&R. É importante que você esteja ciente de que a revogação do seu consentimento não significa que seus dados pessoais serão eliminados, tendo em vista a possiblidade de conservação autorizada dos dados para as finalidades dispostas na lei aplicável.</p>
                    <br>

                    <p><strong>
                            9. Como mantemos as suas informações e seus dados pessoais seguros.
                        </strong></p>

                    <p>Estamos comprometidos com a adoção de políticas e normas de boas práticas relativas à proteção dos dados pessoais. Implementamos as medidas técnicas e organizacionais pertinentes, considerando a tecnologia existente, para a segurança de dados, ainda que não possamos garantir que estejamos imunes a atos ilícitos de terceiros.</p>

                    <p><strong>Segurança de sua informação. </strong>Os sistemas e procedimentos da B&R são estruturados de forma a atender aos requisitos de segurança, aos padrões de boas práticas e aos princípios gerais previstos na legislação aplicável e demais normas regulamentares. A B&R armazenará suas informações e dados pessoais coletados em suas Plataformas Digitais em servidores próprios ou por nós contratados, utilizando os meios razoáveis de mercado e legalmente requeridos para preservar a privacidade sua privacidade, tais como:
                    </p>

                    <ul>
                        <li>Métodos padrões e de mercado para criptografar e anonimizar os dados.</li>

                        <li>Proteção contra acesso não autorizado a seus sistemas.</li>

                        <li>Acesso restrito às pessoas previamente autorizadas ao local onde são
                            armazenados os dados e as informações coletadas.
                        </li>

                        <li>Comprometimento e sigilo absoluto pelas pessoas que tiverem acesso aos
                            dados e informações.
                        </li>

                        <li>Manutenção de inventário indicando momento, duração, identidade do
                            funcionário, ou do responsável pelo acesso com base nos registros de
                            conexão e de acesso a aplicações.
                        </li>
                    </ul>

                    <p><strong>Ressalvas.</strong>É importante que você saiba que nenhum site é totalmente seguro. A B&R adota as melhores práticas de segurança da informação, porém incentivamos você a tomar as medidas apropriadas para reforçar esta proteção, como, por exemplo, mantendo confidenciais todos os nomes de usuário e senhas bem como não transferindo os seus dados de acesso a quaisquer terceiros.</p>
