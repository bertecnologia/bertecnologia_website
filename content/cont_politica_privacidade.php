                    <h3>O que é?</h3>
                    <p>A B&R Engenharia e Tecnologia, preocupa-se com sua privacidade. Acreditamos
                        que você deve estar no controle sobre seus dados pessoais.</p>


                    <p>Por isso, preparamos essa Política de Privacidade para ajudar você a entender melhor como usamos seus
                        dados pessoais em nossas páginas de serviços, sites, aplicativos, softwares, formulários físicos e
                        demais plataformas, aqui chamadas de Plataformas Digitais.</p>
                    <br>

                    <p><strong>1. Para que serve esse documento</strong></p>

                    <p>A Política de Privacidade é importante para uso do software da B&R, na medida em
                        que determina quais dados usamos e coletamos, bem como seus direitos
                        enquanto titular dos seus dados pessoais. É importante que você dê seu aceite
                        caso concorde com este documento, após uma leitura cuidadosa que lhe permita
                        entender todo conteúdo</p>

                    <p> <strong>Objetivo.</strong> Criamos essa Política de Privacidade para ajudar você a entender quais
                        são as práticas adotadas pela B&R ao realizar o tratamento de seus dados
                        pessoais e o motivo pelo qual seus dados são solicitados ou coletados,
                        demonstrando o nosso compromisso com a boa-fé, segurança, privacidade e
                        transparência.</p>

                    <p> <strong>Aplicabilidade.</strong> Esta política é aplicável à todas as pessoas que acessarem nossas
                        páginas de serviço e poderá ser aplicável a outras formas de coleta de dados pela
                        B&R, que permitam qualquer prestação de serviços ou fornecimento de produtos
                        ou conteúdo.</p>

                    <p> <strong>Aceite.</strong> Ao utilizar as Plataformas Digitais, você informa que leu, compreendeu e
                        aceita este documento, além de concordar em ficar vinculado por ele. Caso tenha
                        dúvidas sobre o entendimento a respeito de qualquer disposição desta Política de
                        Privacidade, entre em contato com o DPO (Data Protection Officer, encarregado
                        dos dados) por meio do canal oficial disponível somente para atender os titulares
                        dos dados pessoais, aqui mencionado no final desta Política. Se você não
                        concordar (ou não puder cumprir) com este documento, você não poderá usar as
                        Plataformas Digitais da B&R ou demais serviços que façam uso de seus dados
                        pessoais.</p>

                    <p> <strong>Demais documentos.</strong> Alguns serviços oferecidos pela B&R podem estar sujeitos a
                        termos e condições adicionais especificadas pela B&R ou terceiros
                        periodicamente, os quais serão incorporados nesta Política de Privacidade para os
                        quais você também poderá ter de dar seu consentimento.
                    </p>
                    <br>

                    <p><strong>2. Quando coletamos seus dados pessoais</strong></p>

                    <p>Podemos coletar seus dados diretamente quando o titular informa ao utilizar
                        nossas Plataformas Digitais ou demais produtos ofertados pela B&R ou, ainda,
                        indiretamente por meio de bases de dados públicos ou terceiros parceiros ou não,
                        desde que foi coletado dentro os embasamentos previstos pela Lei Geral de
                        Proteção de Dados 13.709/2018.
                    </p>

                    <p> <strong>Quando. </strong>Quando. Quando você realiza o seu cadastro nas Plataformas Digitais, acessa
                        quaisquer de nossos produtos ou serviços ou participa de eventos, campanhas e
                        promoções, seus dados serão objeto de tratamento, incluindo os seguintes usos:
                    </p>

                    <ul>
                        <li>Utilização de todas funcionalidades das Plataformas Digitais com uma boa
                            experiência de navegação.</li>

                        <li>Inscrições em campanhas, blogs, formulários de promoções.</li>

                        <li>Acesso aos canais de atendimento.</li>

                        <li>Participação em pesquisas e eventos.</li>

                        <li> Recebimento de qualquer informação, aviso de promoções, atualização de
                            conteúdo ou notícia.</li>

                        <li>Armazenamento</li>
                    </ul>

                    <p> <strong>Dados públicos. </strong>A B&R poderá também coletar dados e informações sobre você
                        que estejam disponíveis publicamente ou que forem tornadas públicas por você,
                        incluindo dados e informações sobre menções ou interações com nossas
                        Plataformas Digitais.
                    </p>

                    <p> <strong>Dados com origem de terceiros. </strong>Podemos receber informações sobre você de
                        terceiros, tais como nossas empresas afiliadas, parceiros de publicidade,
                        prestadores de serviços de segurança e qualidade.
                    </p>
                    <br>

                    <p><strong>3. Quais dados pessoais coletamos de você</strong></p>

                    <p> <strong>Dados pessoais são todos aqueles que possam identificá-lo.</strong> Coletamos dados
                        pessoais cadastrais (como nome, e-mail, endereço, telefone, CPF/CNPJ), assim
                        como dados de registro de navegação (como IP de dispositivos, histórico de
                        navegação e cookies), o mínimo necessário para oferecer a melhor experiência ou
                        para ofertar um determinado produto ou serviço requerido.

                    </p>

                    <p> <strong>Dados cadastrais. </strong>Poderão ser utilizadas pela B&R diversos dados pessoais, os
                        quais poderão incluir, mas não se limitam a:
                    </p>

                    <ul>
                        <li> Dados cadastrais, tais como: nome, documentos de identificação,
                            nacionalidade, endereço, data de nascimento, filiação, gênero, entre outros.</li>

                        <li> Dados de contato tais como: telefone, e-mail e endereço.</li>

                        <li> Dados sobre sua atividade, trabalho, profissão.</li>

                        <li> Dados sobre sua renda.</li>

                        <li> Dados coletados de terceiros, incluindo os já acima mencionados, suas
                            fotografias e demais documentos de identificação.</li>

                        <li> Dados de contratações de qualquer serviço ou produto realizados por você
                            nas Plataformas Digitais.</li>
                    </ul>

                    <p> <strong>Dados de navegação. </strong>Poderão ser utilizados pela B&R os seguintes dados de
                        navegação, os quais poderão incluir, mas não se limitam a:

                    </p>

                    <ul>
                        <li>Endereço de IP do dispositivo utilizado por você.</li>

                        <li>Interações realizadas e uso de perfil.</li>

                        <li>Dados técnicos, como informações de URL, conexão de rede, do provedor,
                            do dispositivo.</li>

                        <li>Histórico de navegação e interesses.</li>

                        <li>Atributos de dispositivo, como ID, sistema operacional, navegador e
                            modelo.</li>

                        <li>Acesso a eventuais listas de contatos caso exista a ativação desta
                            funcionalidade.</li>

                        <li>Dados como cookies, pixels tags, beacons e local shared objects.</li>

                        <li>Dados sobre as características dos dispositivos de acesso.</li>

                        <li>Dados do navegador, incluindo IP (com data e hora) e origem do IP</li>

                        <li>Dados e informações sobre cliques, páginas acessadas, páginas seguintes
                            acessadas após a saída das páginas anteriores, termos de buscas
                            digitados em sites e nas Plataformas Digitais.</li>
                    </ul>
                    <br>

                    <p><strong>4. Para que usamos seus dados pessoais</strong></p>

                    <p>Usamos seus dados pessoais que sejam necessários para prestarmos nossos
                        serviços, dentro e fora das Plataformas Digitais, da melhor forma possível,
                        respeitando, sempre suas preferências de privacidade. Podemos fazer uso para
                        fins publicitários, de segurança e de personalização dos serviços.</p>

                    <p><strong>Fins de nossos serviços. </strong>A B&R utiliza os seus dados para o estabelecimento de
                        vínculo contratual ou ainda para gestão, administração, prestação, ampliação e
                        melhoramento dos produtos, serviços, bem como das Plataformas Digitais
                        adequando-as às suas preferências.
                    </p>

                    <p><strong>Fins publicitários. </strong>Os dados pessoais e demais informações coletadas também
                        poderão ser utilizadas para fins publicitários, como envio de informações de
                        marcas, produtos, promoções e descontos, para a divulgação de eventos ou para a
                        realização de pesquisas, sempre respeitando a transparência com o titular dos
                        dados.</p>

                    <p><strong>Fins de segurança. </strong>Para operar nossos serviços, usamos alguns de seus dados,
                        em especial dados de registro e de navegação, para assegurar seu desempenho
                        seguro, confiável e robusto, bem como para garantir a segurança das contas e a
                        devida autenticação.
                    </p>

                    <p><strong>Fins de personalização. </strong>Para personalizar nossos serviços, poderemos associar
                        você a informações fornecidas por você ou por terceiros, por meio de inferências
                        sobre temas sobre os quais você pode estar interessado e da customização do
                        conteúdo que mostramos em nossas Plataformas Digitais, inclusive em anúncios.</p>

                    <p><strong>Demais finalidades. </strong>Os dados pessoais obtidos pela B&R poderão também incluir
                        as seguintes finalidades:</p>

                    <ul>
                        <li>Atendimento de suas solicitações e/ou esclarecimento de dúvidas</li>

                        <li>Contato por telefone, e-mail, SMS, aplicativo de mensagens, inclusive por
                            meio de notificações ou push.</li>

                        <li>Envio de comunicações em geral, incluindo atualizações desta Política de
                            Privacidade bem como de demais termos aplicáveis.</li>

                        <li>Exibição de publicidade nas Plataformas Digitais, redes sociais, inclusive
                            em sites e plataformas de parceiros, assim como envio de ofertas,
                            informações, novidades, atualizações de produtos, serviços, conteúdos,
                            funcionalidades, notícias, eventos relevantes que possam ser de seu
                            interesse e para manutenção de relacionamento com você.</li>

                        <li>Prevenção e resolução de problemas técnicos e de segurança.</li>

                        <li>Prevenção e combate a fraudes e demais atos ilícitos.</li>

                        <li> Colaboração ou cumprimento de ordem judicial de autoridade competente
                            ou órgão fiscalizador, assim como cumprimento de obrigação legal ou
                            regulatória.</li>

                        <li>Melhoria dos sistemas, produtos e serviços.</li>

                        <li>Recomendações e operacionalização de novos serviços, produtos ou
                            funcionalidades de aplicativos, softwares, plataformas, sites, inclusive de
                            serviços oferecidos por parceiros.</li>

                        <li>Geração de estatísticas, estudos, pesquisas e levantamentos pertinentes às
                            atividades e comportamento no uso das Plataformas Digitais, produtos ou
                            serviços.</li>

                        <li>Dados de geolocalização, quando possível.</li>
                    </ul>
