                        garantir que terá acesso a todas nossas funcionalidades.</p>

                    <p><strong>O que são. </strong>Os cookies são arquivos ou informações que podem ser armazenadas
                        em seus dispositivos, quando você visita as Plataformas Digitais.</p>

